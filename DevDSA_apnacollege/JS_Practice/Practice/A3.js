let quarter = 1;

switch (quarter) {
    case 1:
        console.log("Jan, Feb, Mar");
        break;
        case 2:
        console.log("Apr, May, Jun");
        break;
        case 3:
        console.log("Jul, Aug, Sep");
        break;
        case 4:
        console.log("Oct, Nov, Dec");
        break;
        default:
        console.log("Invalid Input");
        break;
}